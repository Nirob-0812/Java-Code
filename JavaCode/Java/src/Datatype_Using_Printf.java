public class Datatype_Using_Printf {

public static void main(String[] args)
{
    boolean b=false;
    char c='N';
    short s=32677;
    int i=5656556;
    float f=32.5565265f;
    double d=25.3652652;
    
    System.out.printf("Boolean: %b\n",b);
    System.out.printf("character: %s\n",c);
    System.out.printf("Short: %d\n",s);
    System.out.printf("Integer: %d\n",i);
    System.out.printf("Float: %.3f\n",f);
    System.out.printf("Double: %.3f\n",d);
    
}
}
